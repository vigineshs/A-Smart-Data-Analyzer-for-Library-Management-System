    private void UpdateSetting()
    {
        int[] link = new int[1];
        int[] power = new int[1];

        Main.ReaderXP.GetCurrentLinkProfile(link);
        Main.ReaderXP.GetPowerLevel(power);

        lbl_freqProfile.setText(Main.ReaderXP.SelectedRegionCodeName());
        if (Main.ReaderXP.IsFixedChannel())
        {
            lbl_fixedFreqTitle.setText("Fixed Frequency :");
            lbl_fixedFreq.setText(Double.toString(Main.ReaderXP.SelectedFrequencyBand())+" MHz");
        }
        else
        {
            lbl_fixedFreqTitle.setText("Frequency :");
            lbl_fixedFreq.setText("Hopping");
        }
        lbl_profile.setText(Integer.toString(link[0]));
        lbl_power.setText(Integer.toString(power[0]));
    }
