    public static boolean SaveSettings()
    {
        Settings.Channel_number = Main.ReaderXP.SelectedChannel();
        Settings.Lbt = Main.ReaderXP.LBT_ON() == LBT.ON;
        Settings.Link_profile = Main.ReaderXP.SelectedLinkProfile();
        Settings.Power = Main.ReaderXP.SelectedPowerLevel();
        Settings.Region = Main.ReaderXP.SelectedRegionCode();
        Settings.FixedChannel = Main.ReaderXP.IsFixedChannel();
        return Settings.Save();
    }

    public static void LoadSettings()
    {
        Settings.Load();
        //Apply previous configuration
        if (Main.ReaderXP.SetPowerLevel(Settings.Power) != CSLibrary.Constants.Result.OK)
        {
            MessageBox.ErrorShow("SetPowerLevel rc = " + Integer.toString(Main.ReaderXP.LastResultCode()));
            return;
        }
        if (Main.ReaderXP.SetCurrentLinkProfile(Settings.Link_profile) != CSLibrary.Constants.Result.OK)
        {
            MessageBox.ErrorShow("SetCurrentLinkProfile rc = " + Integer.toString(Main.ReaderXP.LastResultCode()));
            return;
        }
        if (Settings.FixedChannel)
        {
            if (Main.ReaderXP.SetFixedChannel(Settings.Region, Settings.Channel_number, Settings.Lbt ? LBT.ON : LBT.OFF) != CSLibrary.Constants.Result.OK)
            {
                MessageBox.ErrorShow("SetFixedChannel rc = " + Integer.toString(Main.ReaderXP.LastResultCode()));
                return;
            }
        }
        else
        {
            if (Main.ReaderXP.SetHoppingChannels(Settings.Region) != CSLibrary.Constants.Result.OK)
            {
                MessageBox.ErrorShow("SetHoppingChannels rc = " + Integer.toString(Main.ReaderXP.LastResultCode()));
                return;
            }
        }

        switch (Settings.singulation)
        {
            case SingulationAlgorithm.DYNAMICQ:
                if (Main.ReaderXP.SetSingulationAlgorithmParms(Settings.singulation, Settings.dynamicQ) != CSLibrary.Constants.Result.OK)
                {
                    MessageBox.ErrorShow("SetSingulationAlgorithmParms rc = " + Integer.toString(Main.ReaderXP.LastResultCode()));
                    return;
                }
                break;
            case SingulationAlgorithm.DYNAMICQ_ADJUST:
                if (Main.ReaderXP.SetSingulationAlgorithmParms(Settings.singulation, Settings.dynamicQAdj) != CSLibrary.Constants.Result.OK)
                {
                    MessageBox.ErrorShow("SetSingulationAlgorithmParms rc = " + Integer.toString(Main.ReaderXP.LastResultCode()));
                    return;
                }
                break;
            case SingulationAlgorithm.DYNAMICQ_THRESH:
                if (Main.ReaderXP.SetSingulationAlgorithmParms(Settings.singulation, Settings.dynamicQThres) != CSLibrary.Constants.Result.OK)
                {
                    MessageBox.ErrorShow("SetSingulationAlgorithmParms rc = " + Integer.toString(Main.ReaderXP.LastResultCode()));
                    return;
                }
                break;
            case SingulationAlgorithm.FIXEDQ:
                if (Main.ReaderXP.SetSingulationAlgorithmParms(Settings.singulation, Settings.fixedQ) != CSLibrary.Constants.Result.OK)
                {
                    MessageBox.ErrorShow("SetSingulationAlgorithmParms rc = " + Integer.toString(Main.ReaderXP.LastResultCode()));
                    return;
                }
                break;
        }
    }
